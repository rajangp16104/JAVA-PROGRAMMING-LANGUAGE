public class JavaConditional4{

    public static void main(String args[])
    {
        // Program for Ternary Operator //
        int a = 63, b = 36;
        boolean x = (a < b) ? true : false;
        int y = (a > b) ? a : b;
        System.out.println(" X : " + x);
        System.out.println(" Y : " + y);

    }
}