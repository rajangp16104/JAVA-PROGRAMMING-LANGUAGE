public class JavaSwitch2{

    public static void main(String args[])
    {
        char ch = 'a';
        switch(ch)
        {

            case 'a' : System.out.println("Samosa");
                     break;
            case 'b': System.out.println("Burger");
                     break;
            case 'c' : System.out.println("Mango Shake");
                     break;
            default : System.out.println("No Items");
        }
    }
}