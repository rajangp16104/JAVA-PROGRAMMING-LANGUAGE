public class JavaFuncMethod4
{
    // This is a Program for using functions of math class in java // 
    public static void main(String args[])
    {
        double x = 28;
        double y = 4;
        System.out.println("Maximum number of x and y is : " + Math.max(x , y));
        System.out.println("Minimum number of x and y is : " + Math.min(x , y));
        System.out.println("Square Root y is : " + Math.sqrt(y));
        System.out.println("Power of x and y is : " + Math.pow(x , y));
        System.out.println("Absolute value of x and y is : " + Math.abs(x));

    }
}